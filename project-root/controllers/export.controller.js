const fs = require('fs');
const path = require('path');
const PDFDocument = require('pdfkit');
const { getChatHistory } = require('../services/sessionService');

const DATA_DIR = path.join(__dirname, '../data/processed_papers');

// Helpers for citations
function formatAPA(paper) {
  const authors = (paper.authors || []).join(', ');
  const year = paper.year ? `(${paper.year}).` : '(n.d.).';
  const source = paper.source?.sourceName ? paper.source.sourceName + '.' : '';
  return `${authors} ${year} ${paper.title}. ${source} Retrieved from ResearchAI.`;
}

function formatMLA(paper) {
  const authors = (paper.authors || []).join(', ');
  const year = paper.year || 'n.d.';
  const source = paper.source?.sourceName ? ` ${paper.source.sourceName},` : '';
  return `${authors}. "${paper.title}."${source} ${year}.`;
}

function formatIEEE(paper) {
  const authors = (paper.authors || []).join(', ');
  const year = paper.year || 'n.d.';
  const source = paper.source?.sourceName ? `, ${paper.source.sourceName}` : '';
  return `[1] ${authors}, "${paper.title}"${source}, ${year}.`;
}

function formatBibTex(paper) {
  const citeKey = paper.paperId.replace(/[^a-zA-Z0-9]/g, '');
  const authorStr = (paper.authors || []).join(' and ');
  const yearStr = paper.year ? `\n  year={${paper.year}},` : '';
  const journalStr = paper.source?.sourceName ? `\n  journal={${paper.source.sourceName}},` : '';
  return `@article{${citeKey},\n  title={${paper.title}},\n  author={${authorStr}},${yearStr}${journalStr}\n}`;
}

// Helper for PDF sections
function renderSection(doc, title, content) {
  if (!content) return;
  doc.moveDown(1);
  doc.fontSize(16).font('Helvetica-Bold').text(title);
  doc.moveDown(0.5);
  doc.fontSize(11).font('Helvetica').text(content, { align: 'justify', lineGap: 3 });
}

function sanitizeFilename(name) {
  return name.replace(/[^\\w\\s-]/gi, '').trim().replace(/\\s+/g, '_');
}

async function exportSummaryPDF(req, res) {
  const { paperId } = req.params;
  const jsonPath = path.join(DATA_DIR, `${paperId}.json`);

  if (!fs.existsSync(jsonPath)) {
    return res.status(404).json({ error: 'Paper not found.' });
  }

  try {
    const paper = JSON.parse(fs.readFileSync(jsonPath, 'utf8'));

    if (paper.userId !== req.user.userId) {
      return res.status(403).json({ error: 'Forbidden: You do not own this paper.' });
    }

    const safeTitle = sanitizeFilename(paper.title || paperId);
    
    res.setHeader('Content-Type', 'application/pdf');
    res.setHeader('Content-Disposition', `attachment; filename="ResearchAI_${safeTitle}_Summary.pdf"`);

    const doc = new PDFDocument({ margin: 50, bufferPages: true });
    doc.pipe(res);

    // Header
    doc.fontSize(24).font('Helvetica-Bold').text(paper.title || 'Untitled Paper', { align: 'center' });
    doc.moveDown(0.5);
    
    const authorsStr = (paper.authors && paper.authors.length > 0) ? paper.authors.join(', ') : 'Unknown Authors';
    doc.fontSize(12).font('Helvetica-Oblique').text(authorsStr, { align: 'center' });
    doc.moveDown(0.2);
    
    const yearStr = paper.year || 'Unknown Year';
    doc.fontSize(12).font('Helvetica').text(`Year: ${yearStr}`, { align: 'center' });
    doc.moveDown(1);
    
    doc.moveTo(50, doc.y).lineTo(550, doc.y).stroke();
    doc.moveDown(1);

    // Sections
    let abstractText = "No abstract available.";
    if (paper.chunks && paper.chunks.length > 0) {
      abstractText = paper.chunks[0].chunkText.substring(0, 500) + (paper.chunks[0].chunkText.length > 500 ? '...' : '');
    }
    renderSection(doc, 'Abstract', abstractText);

    const summaryText = paper.summaryPreview || "No summary generated yet.";
    renderSection(doc, 'AI Summary', summaryText);

    if (paper.tags && paper.tags.length > 0) {
      renderSection(doc, 'Key Topics', paper.tags.join(', '));
    }

    // Footer on every page
    const pages = doc.bufferedPageRange();
    for (let i = 0; i < pages.count; i++) {
        doc.switchToPage(i);
        doc.fontSize(9).font('Helvetica-Oblique')
           .text(`Generated by ResearchAI · ${new Date().toISOString().split('T')[0]}`, 
             50, doc.page.height - 40, { align: 'center' });
    }

    doc.end();

  } catch (error) {
    console.error('[Export] Summary PDF generation error:', error);
    if (!res.headersSent) res.status(500).json({ error: 'Internal Server Error' });
  }
}

async function exportCitation(req, res) {
  const { paperId } = req.params;
  const style = (req.query.style || 'apa').toLowerCase();
  const jsonPath = path.join(DATA_DIR, `${paperId}.json`);

  if (!fs.existsSync(jsonPath)) {
    return res.status(404).json({ error: 'Paper not found.' });
  }

  try {
    const paper = JSON.parse(fs.readFileSync(jsonPath, 'utf8'));

    if (paper.userId !== req.user.userId) {
      return res.status(403).json({ error: 'Forbidden: You do not own this paper.' });
    }

    let citation = '';
    switch (style) {
      case 'bibtex': citation = formatBibTex(paper); break;
      case 'mla': citation = formatMLA(paper); break;
      case 'ieee': citation = formatIEEE(paper); break;
      case 'apa':
      default: citation = formatAPA(paper); break;
    }

    res.setHeader('Content-Type', 'text/plain');
    res.send(citation);
  } catch (error) {
    console.error('[Export] Citation error:', error);
    res.status(500).json({ error: 'Internal Server Error' });
  }
}

async function exportChatTranscript(req, res) {
  const { paperId } = req.params;
  const jsonPath = path.join(DATA_DIR, `${paperId}.json`);
  let paperTitle = paperId;
  let hasAccess = false;
  
  if (fs.existsSync(jsonPath)) {
      try {
          const paper = JSON.parse(fs.readFileSync(jsonPath, 'utf8'));
          if (paper.userId === req.user.userId) {
              paperTitle = paper.title || paperId;
              hasAccess = true;
          }
      } catch (e) {}
  }
  
  if(!hasAccess) {
      return res.status(403).json({ error: 'Forbidden or missing paper.' });
  }

  try {
    const history = await getChatHistory(paperId, req.user.userId);
    
    const safeTitle = sanitizeFilename(paperTitle);

    let md = `# Chat Transcript\\n\\n`;
    md += `## Paper: ${paperTitle}\\n`;
    md += `**Exported:** ${new Date().toLocaleString()}\\n\\n---\\n\\n`;

    if (!history || history.length === 0) {
        md += `*No chat history available.*`;
    } else {
        history.forEach(msg => {
            if (msg.role === 'user') {
                md += `### User\\n${msg.content}\\n\\n`;
            } else if (msg.role === 'assistant') {
                md += `### AI\\n${msg.content}\\n\\n`;
            }
        });
    }

    res.setHeader('Content-Type', 'text/markdown');
    res.setHeader('Content-Disposition', `attachment; filename="ResearchAI_${safeTitle}_Transcript.md"`);
    res.send(md);

  } catch (error) {
    console.error('[Export] Chat transcript error:', error);
    res.status(500).json({ error: 'Internal Server Error' });
  }
}

module.exports = {
  exportSummaryPDF,
  exportCitation,
  exportChatTranscript
};
